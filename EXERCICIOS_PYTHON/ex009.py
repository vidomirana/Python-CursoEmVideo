# Tabuada

n = int(input('Escolha um número de 1 a 10: '))

um = n*1
dois = n*2
tres = n*3
quatro = n*4
cinco = n*5
seis = n*6
sete = n*7
oito = n*8
nove = n*9
dez = n*10

print(f'A tabuada do {n} é: ')
print(f' {n} x 1 = {um} \n {n} x 2 = {dois} \n {n} x 3 = {tres} \n {n} x 4 = {quatro} \n {n} x 5 = {cinco}')
print(f' {n} x 6 = {seis} \n {n} x 7 = {sete} \n {n} x 8 = {oito} \n {n} x 9 = {nove} \n {n} x 10 = {dez}')
